    private void jButtonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarActionPerformed
        // TODO add your handling code here:
        RepositorioProducto objRepoProd = new RepositorioProducto();
        IngresarProductoPortInt objProdPortInt = new IngresarProductoPortInt(new IngresarProductos(objRepoProd), objRepoProd);
        Producto objPro = new Producto(this.jTextFieldCodigo.getText(), 
                                this.jTextFieldNombre.getText(), 
                                this.jTextFieldTipo.getText(), 
                                Integer.parseInt(this.jTextFieldCantidad.getText()));
        if (objRepoProd.verificarExistenciaProductos(this.jTextFieldCodigo.getText()) == false) {
            objProdPortInt.ingresarProductos(objPro);
            JOptionPane.showMessageDialog(null, "Se ingres√≥ el producto correctamente");
        }
        else{
            JOptionPane.showMessageDialog(null, "El producto ya existe");
        }
        
    }//GEN-LAST:event_jButtonIngresarActionPerformed

